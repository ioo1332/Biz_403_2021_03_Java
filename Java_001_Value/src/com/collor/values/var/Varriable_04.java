package com.collor.values.var;

public class Varriable_04 {

	public static void main(String[] args) {
		
		// 변수가 초기화 된다(사용할 준비가 된다)
		// 선언과 값의 할당을 동시에 수행
		int dan=7;
		
		System.out.println("==============");
		System.out.print(dan);
		System.out.println("단 구구단"); 
		System.out.println("==============");
		System.out.println(dan+"x1="+(dan*1));
		System.out.println(dan+"x2="+(dan*2));
		System.out.println(dan+"x3="+(dan*3));
		System.out.println(dan+"x4="+(dan*4));
		System.out.println(dan+"x5="+(dan*5));
		System.out.println(dan+"x6="+(dan*6));
		System.out.println(dan+"x7="+(dan*7));
		System.out.println(dan+"x8="+(dan*8));
		System.out.println(dan+"x9="+(dan*9));
		System.out.println("==============");
		
		
		System.out.printf("%dx1=%d\n",dan,dan*1);
		System.out.printf("%dx2=%d\n",dan,dan*2);
		System.out.printf("%dx3=%d\n",dan,dan*3);
		System.out.printf("%dx4=%d\n",dan,dan*4);
		System.out.printf("%dx5=%d\n",dan,dan*5);
		System.out.printf("%dx6=%d\n",dan,dan*6);
		System.out.printf("%dx7=%d\n",dan,dan*7);
		System.out.printf("%dx8=%d\n",dan,dan*8);
		System.out.printf("%dx9=%d\n",dan,dan*9);
				
		
	}
}
