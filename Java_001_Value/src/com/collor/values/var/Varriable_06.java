package com.collor.values.var;

public class Varriable_06 {

	
	public static void main(String[] args) {
		
		//정수형 변수 선언과 clear
		int num1=0;
		Integer num2 = 0;
		
		// int, integer로 선언된 변수는 정수값만 저장할수있다
		// 최대 -2의 31승~ 2의 31승 -1까지 범위만 저장가능
		num1=30;
		num2=30;
		
		// -2의 63승 ~ 2의 63승 -1까지 저장가능
		long numlong1=30l;
		long numlong2=30l;
		 
		// 실수
		float num3 = 30.0f;
		float num4 = 30.0f;

		double num5=30.0;
		double num6=30.0;
		
	}
}
